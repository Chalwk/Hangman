function WordBank:getRandomWord(difficulty, category)
    local wordList = self.words[difficulty][category]
    if not wordList then
        -- Fallback to medium general if category doesn't exist
        wordList = self.words.medium.general
    end

    -- Fallback word
    if #wordList == 0 then return "HANGMAN" end

    return wordList[math_random(#wordList)]
end

return WordBank
